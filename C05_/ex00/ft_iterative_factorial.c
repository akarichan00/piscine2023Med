int	ft_iterative_factorial(int nb)
{
	int	i;
	int	n;

	i = 2;
	n = 1;
	if (nb < 0)
		return (0);
	if (nb == 0 || nb == 1)
		return (1);
	while (i <= nb)
	{
		n *= i;
		i++;
	}
	return (n);
}
